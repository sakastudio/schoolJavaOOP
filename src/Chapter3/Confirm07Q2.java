package Chapter3;

public class Confirm07Q2 {
  public static void main(String[] args){
    int score = 50;
    if(score>=80){
      System.out.println("優です");
    }else if(score>=65){
      System.out.println("良です");
    }else if(score>=50){
      System.out.println("可です");
    }else{
      System.out.println("不可です");
    }
  }
}
