package Chapter3;

public class Confirm06Q2 {
  public static void main(String[] args){
    int x;
    x = 10;
    
    System.out.println(x);
    x--;
    System.out.println(x);
    --x;
    System.out.println(x);
    x = 10;
    System.out.println(x);
    System.out.println(--x);
    System.out.println(x--);
    System.out.println(x);
  }
}
